package com.fabricmanagement.human.employee.domain;

/**
 * Personal title/salutation for global support.
 * 
 * <p>Used in formal communications and display names.</p>
 * 
 * <p><b>Global Support:</b> Covers common titles across cultures.</p>
 */
public enum Title {
    
    /**
     * Mister - Adult male
     */
    MR,
    
    /**
     * Miss - Unmarried female
     */
    MISS,
    
    /**
     * Mrs - Married female
     */
    MRS,
    
    /**
     * Ms - Female (marital status neutral)
     */
    MS,
    
    /**
     * Doctor - Medical or academic
     */
    DR,
    
    /**
     * Professor - Academic
     */
    PROF,
    
    /**
     * Engineer - Professional title
     */
    ENG,
    
    /**
     * No title - Default
     */
    NONE;
}

