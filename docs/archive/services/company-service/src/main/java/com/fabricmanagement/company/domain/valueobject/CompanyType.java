package com.fabricmanagement.company.domain.valueobject;

/**
 * Company Type Enumeration
 * 
 * Represents the type of company organization
 */
public enum CompanyType {
    /**
     * Corporation
     */
    CORPORATION,
    
    /**
     * Limited Liability Company
     */
    LLC,
    
    /**
     * Partnership
     */
    PARTNERSHIP,
    
    /**
     * Sole Proprietorship
     */
    SOLE_PROPRIETORSHIP,
    
    /**
     * Non-Profit Organization
     */
    NON_PROFIT,
    
    /**
     * Government Agency
     */
    GOVERNMENT,
    
    /**
     * Other
     */
    OTHER
}
