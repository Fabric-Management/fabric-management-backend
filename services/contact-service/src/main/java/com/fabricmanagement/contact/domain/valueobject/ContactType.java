package com.fabricmanagement.contact.domain.valueobject;

/**
 * Contact Type Enumeration
 * 
 * Defines different types of contact information
 */
public enum ContactType {
    /**
     * Email address
     */
    EMAIL,
    
    /**
     * Phone number
     */
    PHONE,
    
    /**
     * Physical address
     */
    ADDRESS,
    
    /**
     * Fax number
     */
    FAX,
    
    /**
     * Website URL
     */
    WEBSITE,
    
    /**
     * Social media handle
     */
    SOCIAL_MEDIA
}
